import turtle as t
t.speed("fastest")
t.color("blue")
t.circle(50)
t.color("grey")
t.penup()
t.goto(-15,65)
t.pendown()
t.begin_fill()
t.circle(5)
t.end_fill()
t.penup()
t.goto(-15,65)
t.pendown()
t.penup()
t.goto(15,65)
t.pendown()
t.begin_fill()
t.circle(5)
t.end_fill()
t.penup()
t.color("blue")
t.goto(-15,65)
t.pendown()
t.circle(8)
t.penup()
t.goto(15,65)
t.pendown()
t.circle(8)
t.penup()
t.goto(-15,40)
t.color("red")
t.pendown()
for i in range(3):
    t.forward(25)
    t.right(240)
t.penup()
t.goto(-20,23)
t.pendown()
t.right(90)
t.circle(20,180)
t.right(-90)
t.forward(40)
t.hideturtle()
t.mainloop()